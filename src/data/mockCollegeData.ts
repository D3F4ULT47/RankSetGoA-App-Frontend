export type InstituteType = 'IIT' | 'NIT' | 'IIIT' | 'GFTI' | 'Private';
export type Category = 'GEN' | 'EWS' | 'OBC-NCL' | 'SC' | 'ST' | 'PwD';
export type ExamType = 'JEE Main' | 'JEE Advanced';
export type ExamSelection = 'JEE Main' | 'JEE Advanced' | 'Both';
export type Gender = 'Gender-neutral' | 'Female-only' | 'All';
export type StateQuota = 'Home State' | 'Other State' | 'All';
export type Region = 'North' | 'South' | 'East' | 'West' | 'No preference';
export type BranchPreference = 'Circuital' | 'Non-Circuital' | 'Specific' | 'Any';
export type FeeLevel = 'Low' | 'Moderate' | 'High' | 'Any';
export type ProbabilityTag = 'High' | 'Medium' | 'Low';

export interface RankData {
  crl: Record<Category, number>;
  category: Record<Category, number>;
}

export interface CollegeData {
  id: string;
  collegeName: string;
  branch: string;
  instituteType: InstituteType;
  examType: ExamType;
  openingRank: RankData;
  closingRank: RankData;
  quota: 'All India' | 'Home State' | 'Other State';
  averagePackage: number;
  city: string;
  state: string;
  region: Region;
  branchCategory: 'Circuital' | 'Non-Circuital';
  feeRange: FeeLevel;
}

export interface PersonalizationPreferences {
  region: Region;
  branchPreference: BranchPreference;
  specificBranch?: string;
  feeLevel: FeeLevel;
  instituteTypes: InstituteType[];
  locationPriority: 'Metro' | 'Tier-2' | 'Any';
  focus: string[];
}

