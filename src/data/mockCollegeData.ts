function getRegion(state: string): Region {
  const regionMap: Record<string, Region> = {
    'Delhi': 'North',
    'Uttar Pradesh': 'North',
    'Rajasthan': 'North',
    'Uttarakhand': 'North',
    'Punjab': 'North',
    'Haryana': 'North',
    'Maharashtra': 'West',
    'Gujarat': 'West',
    'Goa': 'West',
    'Karnataka': 'South',
    'Tamil Nadu': 'South',
    'Kerala': 'South',
    'Telangana': 'South',
    'Andhra Pradesh': 'South',
    'West Bengal': 'East',
    'Odisha': 'East',
    'Jharkhand': 'East',
    'Bihar': 'East',
    'Assam': 'East',
  };
  return regionMap[state] || 'No preference';
}

function isCircuital(branch: string): 'Circuital' | 'Non-Circuital' {
  return CIRCUITAL_BRANCHES.some(b => branch.toLowerCase().includes(b.toLowerCase().split(' ')[0]))
    ? 'Circuital'
    : 'Non-Circuital';
}

